// Program to demonstrate the use of escape sequence in java
public class Escape{
	public static void main(String[] args){
		/* 
		 * 	\t - Inserts a tab
		 * 	\b - Inserts a backspace
		 * 	\n - Inserts a new-line
		 * 	\r - Inserts a carriage return
		 * 	\f - Inserts a formfeed
		 * 	\' - Inserts a single quote
		 * 	\" - Inserts a double quote
		 * 	\\ - Inserts a backslash
		**/ 
		
		System.out.println("Escape\tSequence");
		System.out.println();
		System.out.println("Escape\bSequence");
		System.out.println();
		System.out.println("Escape\nSequence");
		System.out.println();
		System.out.println("Escape\rSequence");
		System.out.println();
		System.out.println("Escape\fSequence");
		System.out.println();
		System.out.println("Escape\'Sequence");
		System.out.println();
		System.out.println("Escape\"bSequence");
		System.out.println();
		System.out.println("Escape\\Sequence");
	} 
}