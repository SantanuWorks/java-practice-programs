// Program to demonstrate the concept of Wrapper Class in java - types of wrapper class
public class Wrapper1{
	public static void main(String[] args){
		Boolean a = true;
		System.out.println("Boolean Object, a = "+a);
		
		Byte b = 1;
		System.out.println("Byte Object, b = "+b);
		
		Character c = 'A';
		System.out.println("Character Object, c = "+c);
		
		Float d = 1.1f;
		System.out.println("Float Object, d = "+d);
		
		Integer e = 2;
		System.out.println("Integer Object, e = "+e);
		
		Long f = 2l;
		System.out.println("Long Object, f = "+f);
		
		Short g = 3;
		System.out.println("Short Object, g = "+g);
		
		Double h = 1.2;
		System.out.print("Double Object, h = "+h);
	} 
}